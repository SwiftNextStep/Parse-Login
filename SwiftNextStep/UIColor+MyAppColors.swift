extension UIColor{
    
    class func color1() -> UIColor {
        return UIColor(red: 129/255, green: 109/255, blue: 249/255, alpha: 1)
    }
    
    class func color2() -> UIColor {
        return UIColor(red: 096/255, green: 081/255, blue: 187/255, alpha: 1)
    }
    
    class func greenButton() -> UIColor {
        return UIColor(red: 065/255, green: 117/255, blue: 005/255, alpha: 1)
    }
    
    class func grayForLabels() -> UIColor {
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
    }
    
    class func middleGray() -> UIColor {
        return UIColor(red: (255/2)/255, green: (255/2)/255, blue: (255/2)/255, alpha: 1)
    }
    
}
